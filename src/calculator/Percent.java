package calculator;

/**
 * Class does the percent operation of two specified numbers. Their type are double.
 * @author kossT
 */
public class Percent {

    /**
     *
     * @param a
     * @param b
     * @return percent of first number from
     * second number
     */
    public double percentNumbers(double a, double b){
       return (a / b) * 100; 
       }
}
