package calculator;

/**
 * Class does the subtraction of two specified numbers. Their type is double.
 * @author kossT
 */
public class Subtraction {

    /**
     *
     * @param a
     * @param b
     * @return result of subtraction 
     */
    public double subtractNumbers(double a, double b){
       return a - b;
    }
}
